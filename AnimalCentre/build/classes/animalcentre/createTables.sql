CREATE TABLE "ANIMAL" (
    "ID" BIGINT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    "NAME" VARCHAR(255),
    BIRTHYEAR INTEGER,
    GENDER VARCHAR(31),
    NEUTERED BOOLEAN
);

CREATE TABLE "CUSTOMER" (
    "ID" BIGINT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    "NAME" VARCHAR(255),
    ADDRESS VARCHAR(255),
    PHONE VARCHAR(255)
);

CREATE TABLE ADOPTION (
    "ID" BIGINT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    "DATEOFADOPTION" DATE,
    "DATEOFRETURN" DATE,
    CUSTOMERID BIGINT REFERENCES CUSTOMER (ID),
    ANIMALID BIGINT REFERENCES ANIMAL (ID)
);